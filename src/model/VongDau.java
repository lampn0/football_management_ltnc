public class VongDau {
    int mavong;
    String tenvong;

    public VongDau() {
    }

    public VongDau(int mavong, String tenvong) {
        this.mavong = mavong;
        this.tenvong = tenvong;
    }

    public int getMavong() {
        return mavong;
    }

    public void setMavong(int mavong) {
        this.mavong = mavong;
    }

    public String getTenvong() {
        return tenvong;
    }

    public void setTenvong(String tenvong) {
        this.tenvong = tenvong;
    }

    
}
